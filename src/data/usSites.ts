export const US_STATES: string[] = [...new Set(US_SITES.map(s => s.state))].sort();

/** Group sites by state — used when no state filter is active. */
export function groupByState(sites: LibrarySite[]): SiteSection[] {
  const map = new Map<string, LibrarySite[]>();
  for (const site of sites) {
    if (!map.has(site.state)) map.set(site.state, []);
    map.get(site.state)!.push(site);
  }
  return [...map.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([state, data]) => ({ region: state, data }));
}

/** Group sites by sub-region — used when a specific state is selected. */
export function groupByRegion(sites: LibrarySite[]): SiteSection[] {
  const map = new Map<string, LibrarySite[]>();
  for (const site of sites) {
    if (!map.has(site.region)) map.set(site.region, []);
    map.get(site.region)!.push(site);
  }
  return [...map.entries()]
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([region, data]) => ({ region, data }));
}

/** Master grouping: by state when no state selected, by region within a state when filtered. */
export function groupForBrowse(
  sites: LibrarySite[],
  selectedState: string | null,
): SiteSection[] {
  if (selectedState) {
    const filtered = sites.filter(s => s.state === selectedState);
    // Florida uses a known region order
    if (selectedState === 'Florida') {
      const order = [
        'North Florida', 'Central Florida', 'Southeast Florida',
        'Florida Keys', 'Gulf Coast', 'Northeast Florida', 'Panhandle',
      ];
      const map = new Map<string, LibrarySite[]>();
      for (const site of filtered) {
        if (!map.has(site.region)) map.set(site.region, []);
        map.get(site.region)!.push(site);
      }
      return order
        .filter(r => map.has(r))
        .map(region => ({ region, data: map.get(region)! }));
    }
    return groupByRegion(filtered);
  }
  return groupByState(sites);
}
