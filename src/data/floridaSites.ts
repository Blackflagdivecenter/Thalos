// ── Helper: group sites by region ─────────────────────────────────────────────

export interface SiteSection {
  region: SiteRegion;
  data: LibrarySite[];
}

export function groupByRegion(sites: LibrarySite[]): SiteSection[] {
  const order: string[] = [
    'North Florida',
    'Central Florida',
    'Southeast Florida',
    'Florida Keys',
    'Gulf Coast',
    'Northeast Florida',
    'Panhandle',
  ];
  const map = new Map<SiteRegion, LibrarySite[]>();
  for (const site of sites) {
    if (!map.has(site.region)) map.set(site.region, []);
    map.get(site.region)!.push(site);
  }
  return order
    .filter((r) => map.has(r))
    .map((region) => ({ region, data: map.get(region)! }));
}

